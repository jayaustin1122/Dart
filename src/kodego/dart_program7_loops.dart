void main(){

  //while loop.
  int counter = 1;
  while(counter < 5){
    print("hello");
    counter++;
  }
  //-----------------------------------------

  //do while loop
  int counter2 = 1;
  do{
    print("Hello");
    counter++;
  }while(counter2 < 0);
  //-----------------------------------------

  // for loop
  for (int counter3 = 1; counter3 < 5; counter3++){
    print("hello");
  }
}